// `Ученик прошёл диагностику ЕГЭ по [предмет].
//  Правильных ответов: [score]%. Слабые темы: [topics].
//  Напиши 2 предложения — конкретный совет с чего начать подготовку.`
export function getMockAIRecommendation(score, weakTopics, subject) {
  const subjectName = { cs: 'информатике', math: 'математике', russian: 'русскому языку' }[subject] ?? 'предмету'
  const firstWeak = weakTopics[0]?.topic ?? 'базовым темам'

  if (score === 100) {
    return `Отлично! У тебя сильная база по ${subjectName}. Переходи сразу к сложным уровням — ты готов к реальным задачам ЕГЭ.`
  }
  if (score >= 70) {
    return `Хорошая база! Больше всего ошибок в теме «${firstWeak}» — начни именно с неё. После неё пройди остальные слабые темы и ты будешь готов.`
  }
  if (score >= 40) {
    return `Есть над чем поработать. Рекомендую начать с теории в «${firstWeak}» — там базовые концепции, без которых сложно двигаться дальше. По одной теме в день и за месяц закроешь всё.`
  }
  return `Не переживай — ты в начале пути! Начни с самого первого раздела и изучи теорию перед практикой. Главное — регулярность: 15–20 минут каждый день дадут результат за пару недель.`
}
