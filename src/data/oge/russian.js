function rotate(items, seed) {
  const shift = seed % items.length;
  return items.map((_, i) => items[(i + shift) % items.length]);
}

function makeTask(section, level, n) {
  const source = data[section.taskNumber][(level + n - 2) % 5];
  const [prompt, answer, wrong] = source;
  const seed = section.taskNumber * 19 + level * 7 + n;
  const variants = rotate([answer, ...wrong], seed);
  const difficulty = ['простое правило', 'правило с дополнительной проверкой', 'два признака одновременно', 'ловушка формулировки', 'формат, близкий к реальному ОГЭ'][level - 1];
  return {
    id: `${section.id}_l${level}_t${n}`,
    text: `${prompt} Уровень: ${difficulty}.`,
    options: variants,
    correct: variants.indexOf(answer),
    explanation: {
      error: 'Типичная ошибка — выбрать вариант по похожему слову, не проверив правило или смысл.',
      steps: [
        'Прочитай всё предложение или мини-текст полностью.',
        `Определи, какое правило проверяет тема «${section.label}».`,
        'Сравни каждый вариант с правилом и отбрось противоречащие.',
      ],
      tip: 'В русском языке сначала находи основу правила, а потом сверяй варианты.',
    },
  };
}

function makeTheory(section) {
  const theory = theoryMap[section.taskNumber];
  return {
    title: `Задание №${section.taskNumber} — ${section.label}`,
    intro: `В этом задании ОГЭ проверяется тема «${section.label.toLowerCase()}». Нужно внимательно читать предложение или небольшой текст и применять конкретное языковое правило, а не угадывать по звучанию.`,
    blocks: [
      {
        type: 'formulas',
        title: '📐 Формулы / Правила',
        items: theory.formulas.map(([label, formula]) => ({ label, formula })),
      },
      {
        type: 'remember',
        title: '💡 Важно запомнить',
        items: theory.remember,
      },
      {
        type: 'notes',
        title: '📓 Запиши в конспект',
        items: [
          `Краткое определение темы «${section.label}».`,
          '2-3 примера с правильным объяснением.',
          'Список ошибок, из-за которых чаще всего выбирают неверный вариант.',
        ],
      },
    ],
  };
}

function makeLevels(section) {
  return [
    { index: 0, theory: makeTheory(section) },
    ...[1, 2, 3, 4, 5].map((level) => ({
      index: level,
      tasks: [1, 2, 3, 4, 5].map((n) => makeTask(section, level, n)),
    })),
  ];
}

export default sectionDefs.map((section) => ({
  ...section,
  levels: makeLevels(section),
}));
