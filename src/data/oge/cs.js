function rotate(items, seed) {
  const shift = seed % items.length;
  return items.map((_, i) => items[(i + shift) % items.length]);
}

function makeTask(id, text, answer, wrong, seed, steps, tip) {
  const variants = rotate([String(answer), ...wrong.map(String)], seed);
  return {
    id,
    text,
    options: variants,
    correct: variants.indexOf(String(answer)),
    explanation: {
      error: 'Типичная ошибка — считать в неправильных единицах или пропустить один шаг алгоритма.',
      steps,
      tip,
    },
  };
}

function csTask(section, level, n) {
  const id = `${section.id}_l${level}_t${n}`;
  const seed = section.taskNumber * 23 + level * 5 + n;
  const a = level + n + 1;
  const b = level * 2 + n + 2;

  switch (section.taskNumber) {
    case 1: {
      const i = level + 2;
      const k = 10 + n * 2;
      const answer = k * i;
      return makeTask(id, `В алфавите каждый символ кодируется ${i} битами. Сколько бит занимает сообщение из ${k} символов?`, answer, [k + i, k * 8, answer / 2], seed, [`Используем V = K*i.`, `K = ${k}, i = ${i}.`, `V = ${answer} бит.`], 'Для текста умножай число символов на бит на символ.');
    }
    case 2: {
      const value = 8 * level + n;
      const binary = value.toString(2);
      return makeTask(id, `Переведите число ${value} из десятичной системы в двоичную.`, binary, [(value + 1).toString(2), String(value), value.toString(8)], seed, ['Делим число на 2 с остатками.', 'Записываем остатки снизу вверх.', `Получаем ${binary}.`], 'При переводе в двоичную систему остатки читаются в обратном порядке.');
    }
    case 3: {
      const x = n % 2 === 0;
      const y = level % 2 === 1;
      const answer = x && !y ? 'истина' : 'ложь';
      return makeTask(id, `Даны X=${x ? 'истина' : 'ложь'}, Y=${y ? 'истина' : 'ложь'}. Найдите значение выражения X И НЕ Y.`, answer, ['истина, если Y истинно', 'истина всегда', answer === 'истина' ? 'ложь' : 'истина'], seed, ['Сначала выполняем НЕ Y.', 'Потом вычисляем X И результат.', `Ответ: ${answer}.`], 'В логике сначала считай НЕ, затем И, затем ИЛИ.');
    }
    case 4: {
      const repeats = level + n;
      const answer = repeats * 2;
      return makeTask(id, `Исполнитель повторяет ${repeats} раз команду "вперёд 2". На сколько клеток он сдвинется?`, answer, [repeats, answer + 2, answer - 1], seed, [`Одна команда даёт сдвиг 2.`, `Повторов: ${repeats}.`, `Сдвиг: ${repeats} * 2 = ${answer}.`], 'В циклах умножай эффект команды на число повторений.');
    }
    case 5: {
      const start = level + n;
      const answer = start * 2 + 3;
      return makeTask(id, `Переменная x равна ${start}. Выполнены команды: x = x * 2; x = x + 3. Что станет с x?`, answer, [start + 5, start * 2, answer + 3], seed, [`После первой команды x = ${start * 2}.`, `После второй x = ${start * 2} + 3.`, `Итог: ${answer}.`], 'При трассировке записывай значение после каждой строки.');
    }
    case 6: {
      const w = 10 + n;
      const h = 10 + level;
      const depth = 2;
      const answer = w * h * depth;
      return makeTask(id, `Изображение ${w} на ${h} пикселей кодируется глубиной ${depth} бита. Найдите объём в битах.`, answer, [w * h, answer * 8, w + h + depth], seed, [`V = ширина * высота * глубина.`, `V = ${w} * ${h} * ${depth}.`, `V = ${answer} бит.`], 'Для изображения важны пиксели и битовая глубина.');
    }
    case 7: {
      const speed = 8 * (level + 1);
      const time = n + 2;
      const answer = speed * time;
      return makeTask(id, `Канал передаёт ${speed} Кбит/с. Сколько Кбит будет передано за ${time} с?`, answer, [speed + time, answer / 8, answer + speed], seed, [`I = v*t.`, `I = ${speed} * ${time}.`, `I = ${answer} Кбит.`], 'В сетях не смешивай биты и байты.');
    }
    case 8:
    case 10: {
      const x = level + n;
      const y = n + 3;
      const answer = x + y;
      return makeTask(id, `В ячейке A1 число ${x}, в A2 число ${y}. Что даст формула =СУММ(A1:A2)?`, answer, [x * y, x - y, `${x}${y}`], seed, ['Функция СУММ складывает числа в диапазоне.', `${x} + ${y} = ${answer}.`, 'Текстовое склеивание здесь не выполняется.'], 'В формулах таблиц внимательно смотри на диапазон.');
    }
    case 9:
    case 11: {
      const older = level + n;
      const total = older + b;
      return makeTask(id, `В таблице ${total} записей. Условию "возраст больше 14" соответствуют ${older} записей. Сколько записей останется после фильтра?`, older, [total, b, total - older + 1], seed, ['Фильтр оставляет только подходящие записи.', `Подходящих записей: ${older}.`, `Останется ${older}.`], 'Для условий И/ИЛИ сначала считай записи, которые реально проходят фильтр.');
    }
    case 12: {
      const nums = [a, b, a + b, b + 1, a + 2];
      const answer = nums.filter((value) => value % 2 === 0).length;
      return makeTask(id, `Программа просматривает числа ${nums.join(', ')} и считает чётные. Какое значение счётчика получится?`, answer, [answer + 1, nums.length, nums.length - answer], seed, ['Проверяем каждое число на делимость на 2.', 'Увеличиваем счётчик только для чётных.', `Счётчик равен ${answer}.`], 'Счётчик меняется только когда условие истинно.');
    }
    case 13: {
      const nums = [a, b, a + 1, b + 2, a + b];
      const limit = level + n + 3;
      const answer = nums.filter((value) => value > limit).length;
      return makeTask(id, `Массив: ${nums.join(', ')}. Сколько элементов больше ${limit}?`, answer, [answer + 1, nums.length - answer, limit], seed, ['Сравниваем каждый элемент с границей.', 'Считаем только элементы строго больше.', `Получаем ${answer}.`], 'Слова "больше" и "не меньше" задают разные условия.');
    }
    case 14: {
      const word = ['информатика', 'алгоритм', 'таблица', 'данные', 'программа'][(level + n) % 5];
      const answer = word.length;
      return makeTask(id, `Строка равна "${word}". Чему равна её длина?`, answer, [answer - 1, answer + 1, word.split('а').length - 1], seed, ['Считаем все символы строки.', 'Пробелы тоже считались бы символами, но здесь их нет.', `Длина: ${answer}.`], 'Длина строки — это количество символов, а не количество разных букв.');
    }
    case 15: {
      const variants = level + 2;
      const checks = n + 3;
      const answer = variants * checks;
      return makeTask(id, `Алгоритм перебирает ${variants} значения первой переменной и ${checks} значения второй. Сколько пар будет проверено?`, answer, [variants + checks, answer - variants, answer + checks], seed, ['Для каждой первой переменной перебираются все вторые.', `Всего ${variants} * ${checks}.`, `Будет проверено ${answer} пар.`], 'В полном переборе независимые количества перемножаются.');
    }
    default:
      return makeTask(id, `Выберите верный ответ по теме «${section.label}».`, a + b, [a * b, a - b, b - a], seed, [`Берём данные ${a} и ${b}.`, 'Применяем базовое правило темы.', `Ответ: ${a + b}.`], 'Сначала определи тип задачи, потом считай.');
  }
}

function makeTheory(section) {
  const rules = theoryRules[section.taskNumber] || [['Правило', section.description]];
  return {
    title: `Задание №${section.taskNumber} — ${section.label}`,
    intro: `В этом задании ОГЭ проверяется тема «${section.label.toLowerCase()}». Важно перевести условие в короткий алгоритм: какие данные даны, какое правило нужно применить и в каких единицах получить ответ.`,
    blocks: [
      {
        type: 'formulas',
        title: '📐 Формулы / Правила',
        items: rules.map(([label, formula]) => ({ label, formula })),
      },
      {
        type: 'remember',
        title: '💡 Важно запомнить',
        items: [
          'В информатике часто ошибаются не в формуле, а в единицах: биты, байты, Кбит, Кбайт.',
          'Для алгоритмов веди таблицу переменных после каждой команды.',
          'Для логики сначала выполняй НЕ, затем И, затем ИЛИ.',
        ],
      },
      {
        type: 'notes',
        title: '📓 Запиши в конспект',
        items: [
          `Основные правила по теме «${section.label}».`,
          'Мини-алгоритм решения: данные → формула или трассировка → проверка ответа.',
          'Типичные ловушки: неверное основание системы счисления, забытый цикл, биты вместо байтов.',
        ],
      },
    ],
  };
}

function makeLevels(section) {
  return [
    { index: 0, theory: makeTheory(section) },
    ...[1, 2, 3, 4, 5].map((level) => ({
      index: level,
      tasks: [1, 2, 3, 4, 5].map((n) => csTask(section, level, n)),
    })),
  ];
}

export default sectionDefs.map((section) => ({
  ...section,
  levels: makeLevels(section),
}));
