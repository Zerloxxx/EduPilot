// Мержим первые 7 разделов из subject-файлов с остальными из основного массива.
// Матчинг по taskNumber (не по id) — id в subject-файлах может отличаться.
// Сохраняем оригинальный id из fullSections чтобы не сломать сохранённый прогресс.
function mergeFirst7(richFirst7, fullSections) {
  return fullSections.map(section => {
    const rich = richFirst7.find(r => r.taskNumber === section.taskNumber)
    if (!rich) return section
    return { ...rich, id: section.id }
  })
}

// OGE-секции хранят только метаданные (без levels).
// Добавляем пустую структуру levels чтобы остальной код не падал.
function normalizeOgeSections(sections) {
  return sections.map(s => s.levels ? s : {
    ...s,
    levels: [
      { index: 0, theory: { title: s.label, intro: s.description, blocks: [] } },
      { index: 1, tasks: [] },
      { index: 2, tasks: [] },
      { index: 3, tasks: [] },
      { index: 4, tasks: [] },
      { index: 5, tasks: [] },
    ],
  })
}

// ─── Helper: inject input tasks into level 3 of any sections array ────────────
function addInputTasksToLevel3(sections, taskMap) {
  return sections.map(section => {
    const extra = taskMap[section.id]
    if (!extra || !extra.length) return section
    return {
      ...section,
      levels: section.levels.map(level =>
        level.index === 3
          ? { ...level, tasks: [...(level.tasks ?? []), ...extra] }
          : level
      ),
    }
  })
}

